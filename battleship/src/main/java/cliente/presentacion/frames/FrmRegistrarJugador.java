    private void initManejadores() {
        manejadoresNoti = new HashMap<>();
        manejadoresNoti.put("ERROR_UNIRSE", this::manejarErrorUnirse);
    }

    @Override
    public void notificar(String contexto, Object datos) {
        Consumer<Object> manejador = manejadoresNoti.get(contexto);
        if (manejador != null) {
            SwingUtilities.invokeLater(() -> manejador.accept(datos));
        }
    }

    private void manejarErrorUnirse(Object datos) {
        if (datos instanceof RespuestaUnirseDTO respuesta) {
            esperandoRespuesta = false;
            btnContinuar.setEnabled(true);
            nombreJugador.setEnabled(true);

            JOptionPane.showMessageDialog(this,
                    respuesta.getMensaje(),
                    "Error al unirse",
                    JOptionPane.ERROR_MESSAGE);

            // Limpiar el campo de nombre para que ingrese otro
            nombreJugador.setText("");
            nombreJugador.requestFocus();
        }
    }

    private void configurarComponentesAdicionales() {
        grupoColores = new ButtonGroup();
        grupoColores.add(cBoxRojo);
        grupoColores.add(cBoxAzul);

        this.setLocationRelativeTo(null);
    }

    private ColorJugador getColorSeleccionado() {
        if (cBoxRojo.isSelected()) {
            return ColorJugador.ROJO;
        }
        if (cBoxAzul.isSelected()) {
            return ColorJugador.AZUL;
        }

        return null;
    }

